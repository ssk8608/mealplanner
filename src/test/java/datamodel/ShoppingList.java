package datamodel;

public class ShoppingList {
    String item;
    String asile;
    boolean parse;

    public String getItem() {
        return item;
    }

    public void setItem(String item) {
        this.item = item;
    }

    public String getAsile() {
        return asile;
    }

    public void setAsile(String asile) {
        this.asile = asile;
    }

    public boolean isParse() {
        return parse;
    }

    public void setParse(boolean parse) {
        this.parse = parse;
    }

}
